# Import the turtle module and use it to draw a coloured face!
import turtle
t = turtle.Turtle()
t.shape('turtle')

t.penup()
t.begin_fill()
t.color('orange')
t.goto(30,-150)
t.pendown()
t.circle(130)
t.penup()
t.end_fill()
t.color('white')
t.goto(0,0)
t.begin_fill()
t.pendown()
t.circle(20)
t.penup()
t.end_fill()
t.begin_fill()
t.color('black')
t.pendown()
t.circle(10)
t.penup()
t.end_fill()
t.forward(60)
t.right(45)
t.begin_fill()
t.color('white')
t.pendown()
t.circle(30)
t.penup()
t.end_fill()
t.begin_fill()
t.color('black')
t.pendown()
t.circle(10)
t.penup()
t.end_fill()
t.right(90)
t.forward(90)
t.begin_fill()
t.color('white')
t.pendown()
t.circle(40)
t.penup()
t.end_fill()
t.goto(25,-25)
